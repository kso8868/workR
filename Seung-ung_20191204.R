# 다음은 직장인 10명의 수입과 교육받은 기간을 조사한 자료이다. 산점도와 상관계수를 구하고, 
# 수입과 교육기간 사이에 어떤 상관관계가 있는지 설명하시오.
# 
# 수입 	 121 99 41 35 40 29 35 24 50 60
# 교육기간 19  20 16 16 18 12 14 12 16 17

Income <- c(121, 99, 41, 35, 40, 29, 35, 24, 50, 60)
edutime <- c(19, 20, 16, 16, 18, 12, 14, 12, 16, 17)
workman<- data.frame(Income,edutime)

plot(Income, edutime, main = "수입-교육기간 그래프",
     xlab='Income', ylab='edutime',
     col="red", pch=19)
cor(workman[,1:2])                #상관관계가 0.7929108으로 두 변수간에 관련성(양의 상관계수:비례)이 크다.



