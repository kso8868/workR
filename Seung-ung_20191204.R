# 다음은 대학생 10명의 성적과 주당 TV 시청시간을 조사한 자료이다. 산점도와 상관계수를 구하고, 
# 성적과 TV 시청시간 사이에 어떤 상관관계가 있는지 설명하시오.
# 
# 성적 	 77.5 60 50 95 55 85 72.5 80 92.5 87.5
# 시청시간 14   10 20  7 25  9 15   13  4   21

score <- c(77.5, 60, 50, 95, 55, 85, 72.5, 80, 92.5, 87.5)
tv_watch <- c(14, 10, 20, 7, 25, 9, 15, 13, 4, 21)
student<- data.frame(score, tv_watch)
plot(tv_watch, score, main = "성적-TV시청 그래프",
     xlab='tv_watch', ylab='score',
     col="red", pch=19)
cor(student[,1:2])                #상관관계가 -0.6283671으로 두 변수간에 관련성(음의 상관계수:반비례)이 있다.


